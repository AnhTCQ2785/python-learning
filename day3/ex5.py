"Hello, World!".lower()       # "hello, world!"+ biến toàn bộ chữ thành chữ thường
"Hello, World!".upper()       # "HELLO, WORLD!"+ biến toàn bộ chữ thành chữ hoa
"Hello, World!".capitalize()  # "Hello, world!"+ biến toàn bộ chữ thành chữ đầu ghi hoa còn lại ghi thường
"hello, world!".title()       # "Hello, World!"+ biến thành một chuỗi với mỗi chữ là chữ in hoa còn lại là chữ thường
"  Hello, World!  ".strip()  # "Hello, World!"+ loại bỏ dấu cách ở hai bên
"  Hello, World!  ".strip().title()  # "Hello, World!"